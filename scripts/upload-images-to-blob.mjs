import { put, list } from "@vercel/blob";
import { readFileSync, readdirSync, writeFileSync } from "fs";
import { join } from "path";

const IMAGE_DIR = join(process.cwd(), "public/images/explore");
const OUTPUT_MAP_FILE = join(process.cwd(), "lib/blob-image-urls.ts");

async function main() {
  // List existing blobs to avoid re-uploading
  const existing = await list({ prefix: "explore/" });
  const existingPaths = new Set(existing.blobs.map((b) => b.pathname));

  const files = readdirSync(IMAGE_DIR).filter((f) =>
    /\.(jpg|jpeg|png|webp)$/i.test(f)
  );

  console.log(`Found ${files.length} images to upload`);

  const urlMap = {};

  for (const file of files) {
    const blobPath = `explore/${file}`;

    // Check if already uploaded
    const existingBlob = existing.blobs.find((b) => b.pathname === blobPath);
    if (existingBlob) {
      console.log(`  SKIP (exists): ${file} -> ${existingBlob.url}`);
      urlMap[`/images/explore/${file}`] = existingBlob.url;
      continue;
    }

    const filePath = join(IMAGE_DIR, file);
    const fileBuffer = readFileSync(filePath);

    const blob = await put(blobPath, fileBuffer, {
      access: "public",
      contentType: file.endsWith(".png") ? "image/png" : "image/jpeg",
    });

    console.log(`  DONE: ${file} -> ${blob.url}`);
    urlMap[`/images/explore/${file}`] = blob.url;
  }

  // Generate TypeScript mapping file
  const tsContent = `// Auto-generated by scripts/upload-images-to-blob.mjs
// Maps local /images/explore/* paths to Vercel Blob URLs

const BLOB_URLS: Record<string, string> = ${JSON.stringify(urlMap, null, 2)};

/**
 * Resolves an image path to its Vercel Blob URL.
 * Falls back to the original path if no Blob URL exists.
 */
export function blobUrl(localPath: string): string {
  return BLOB_URLS[localPath] || localPath;
}

export default BLOB_URLS;
`;

  writeFileSync(OUTPUT_MAP_FILE, tsContent, "utf-8");
  console.log(`\nGenerated ${OUTPUT_MAP_FILE} with ${Object.keys(urlMap).length} entries`);
}

main().catch(console.error);
